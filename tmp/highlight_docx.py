def get_para_format(para):
    """获取段落第一个有效run的格式"""
    for run in para.runs:
        if run.text.strip():
            return {
                'font_name': run.font.name,
                'font_size': run.font.size,
                'bold': run.bold,
                'italic': run.italic,
                'color': run.font.color.rgb if run.font.color and run.font.color.rgb else None,
            }
    return {'font_name': None, 'font_size': None, 'bold': None, 'italic': None, 'color': None}


def clear_para(para):
    """清除段落中所有w:r元素"""
    p_element = para._element
    for r in p_element.findall('{%s}r' % WML_NS):
        p_element.remove(r)


def apply_run_format(run, fmt, is_highlight, is_red):
    """应用格式到run"""
    if fmt.get('font_name'):
        run.font.name = fmt['font_name']
    if fmt.get('font_size'):
        run.font.size = fmt['font_size']
    if fmt.get('italic'):
        run.italic = fmt['italic']
    # 颜色：红色（修改过的）或黑色（原文）
    if is_red:
        run.font.color.rgb = RED
    elif fmt.get('color'):
        run.font.color.rgb = fmt['color']
    # 加粗+下划线
    if is_highlight:
        run.bold = True
        run.underline = True
    else:
        if fmt.get('bold'):
            run.bold = fmt['bold']


def rebuild_para(para, segments, is_red=False):
    """
    用分段列表重建段落
    segments: [(text, is_highlight), ...] 
    is_red: 是否为已修改段落（保持红色）
    """
    fmt = get_para_format(para)
    clear_para(para)
    for text, is_highlight in segments:
        if text:
            run = para.add_run(text)
            apply_run_format(run, fmt, is_highlight, is_red)
    # 如果完全没有内容，加一个空run
    if not para.runs:
        para.add_run('')


